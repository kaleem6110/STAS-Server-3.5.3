
function LineStyle(color,width){
	this.color = color;
	this.width = width;
}

function IconStyle(color,scale,heading,icon){
	this.color = color;
	this.scale = scale;
	this.heading = heading;
	this.icon = icon;
}

function ListStyle(listItemType,bgColor,ItemIcon){
	this.listItemType = listItemType;
	this.bgColor = bgColor;
	this.ItemIcon = ItemIcon;
}

function LabelStyle(color,scale){
	this.color = color;
	this.scale = scale;
}

function PolyStyle(color){
	this.color = color;
}

function BalloonStyle(bgColor,text){
	this.bgColor = bgColor;
	this.text = text;
}

